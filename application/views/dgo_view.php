<h1>ДГО</h1>
<p><span class="greenb">ДГО</span>  - аббревиатура от "Добровольное страхование  Гражданской Ответственности", также упоминаемое как ДАГО, ДГО, АГО. Полис ДГО является необязательным документом, дополнительно приобретаемым к полису <a href="/avtostrahovanie/osago">ОСАГО</a>.</p>
<p>Лимит ответственности, обеспечиваемый полисом ОСАГО, может быть увеличен за счет приобретения полиса добровольного страхования гражданской ответственности (ДГО).<p>
<span class="greenb">Важно!</span> Полис ДГО не является заменой полиса ОСАГО.
