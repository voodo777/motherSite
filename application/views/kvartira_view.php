<h2>Перепланировка</h2>
<p>Полис "Перепланировка" предлагает страхование ответственности подрядчика при проведении переустройства и ремонта жилого помещения.</p>
<p>Вы можете застраховать ответственность подрядчика за ущерб, нанесенный его действиями третьим лицам при установке, замене или переносе инженерных сетей, санитарно-технического, электрического или другого оборудования и коммуникаций, а также при проведении отделочных работ (косметического ремонта, переноса стен, перегородок).</p>
Полис "РЕСО-Перепланировка" возместит:
<ul>
    <li>ущерб, причиненный имуществу и жизни или здоровью третьих лиц (включая соседей).</li>
    <li>ваши материальные расходы в судебных органах при наступлении страхового случая (в размере 5% от страховой суммы).</li>
</ul>
Это важно:
<ul>
    <li>договор страхования может заключить юридическое лицо или дееспособное физическое лицо. При этом может быть застрахован риск ответственности только юридического лица, имеющего лицензию на проведение соответствующих работ;</li>
    <li>не принимаются на страхование жилые помещения в домах с деревянными перекрытиями;</li>
    <li>срок действия договора страхования ограничивается периодом проведения строительно-монтажных работ;</li>
    <li>полис "РЕСО-Перепланировка" продается только в Москве, Московской области, Санкт-Петербурге и Ленинградской области;</li>
    <li>оплата полиса производится единовременно.</li>
</ul>

<h2>Гражданская ответственность</h2>
<p>Также мы предлагаю застраховать свою гражданскую ответственность за причинение вреда жизни, здоровью и имуществу третьих лиц. Это поможет Вам избежать расходов на возмещение вреда соседям.</p>
<p>Например, в случае если начавшийся у Вас пожар перекинется на соседнюю квартиру. Сорванный водопроводный кран или испорченная канализация также может доставить массу неприятностей не только Вам, но и целому подъезду.</p>
<p>В этом случае  владельцу или арендатору такого "невезучего" жилья придется не только тратить деньги на восстановление собственного имущества, но и возместить ущерб соседям - в этом и заключается его гражданская ответственность перед третьими лицами. Если же вы предусмотрительно застраховали этот риск, то подобные ситуации для вас не страшны, Страховая Компания  возместит  возникший ущерб всем, кто пострадал по вашей вине.</p>
