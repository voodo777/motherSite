<h1>Страхование квартир, таунхаусов</h1>
<p>Различные варианты защиты Вашей квартиры и домашнего имущества.</p>

<h2>Домовой</h2>
<p>Страховой полис "Домовой" не заменит сказочного хранителя домашнего очага, но вполне может стать гарантом Вашего спокойствия и благополучия.</p>
<p>В квартире подлежат страхованию:
    <ul class="ULnum">
        <li><span class="greenb">Конструкция квартиры</span> – конструктивные элементы здания (стены, перекрытия), оконные и дверные конструкции, трубопроводы различных систем.</li>
        <li><span class="greenb">Отделка</span> – покрытие стен, потолка, пола, межкомнатные двери, встроенная мебель.</li>
        <li><span class="greenb">Техническое оборудование</span> – системы отопления, подогрева пола, водоснабжения, канализации, газоснабжения, системы наблюдения и охраны, вентиляции, сантехническое оборудование и т.д.</li>
        <li><span class="greenb">Движимое имущество</span> – мебель, предметы интерьера, аудио- и видеоаппаратура, электроника, бытовая техника, музыкальные инструменты, одежда, посуда, инструменты, предметы домашнего обихода и т.д.</li>
        <li><span class="greenb">Гражданская ответственность</span> за причинение вреда жизни, здоровью или ущерба имуществу третьих лиц (лимит ответственности – до 3 000 000 руб.).</li>
        <li><span class="greenb">Жизнь и здоровье жильцов</span> квартиры от несчастного случая* (лимит ответственности – до 300 000 руб. на каждого).</li>
    </ul>
</p>
<p>Полис "Домовой" позволяет свободно комбинировать объекты и выбирать суммы страховой защиты по каждому из них.
<br><span class="remark">* Согласно Правилам страхования действие страховой защиты распространяется на всю территорию РФ.</span></p>

<h2>Домовой - лучший вариант</h2>
Лучший вариант страхования квартир по полному пакету рисков, включающему:
<li>пожар, удар молнии, взрыв газа;</li>
<li>повреждение имущества водой или паром вследствие аварий различных систем или залив жидкостью, поступившей из других жилых помещений;</li>
<li>стихийные бедствия;</li>
<li>кража со взломом, грабеж;</li>
<li>противоправные действия третьих лиц – хулиганство, вандализм, умышленное уничтожение и повреждение имущества;</li>
<li>столкновения, удар – наезд транспортных средств, столкновения с животными, падение деревьев и иных предметов, падения летательных аппаратов и их частей, удары звуковых волн;</li>
<br>и дополнительный риск:
<li>терроризм, диверсия – совершение взрыва, поджога или иных действий в целях нарушения общественной безопасности.</li>

