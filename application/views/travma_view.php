<h1>Страхование от несчастных случаев</h1>
<p>Застраховавшись от несчастного случая  Вы получите качественную страховую защиту. По Вашему выбору эта защита может действовать от одного дня до одного года, круглосуточно или в определенные часы, по всему миру или на ограниченной территории.</p>
<p>Вне зависимости от того, учится Ваш ребенок в школе или только готовится к ней, отправился он в летний лагерь или поехал к бабушке, Вы всегда тревожитесь о нем. Благодаря страховому полису Вы получаете реальную возможность компенсировать расходы на восстановление здоровья ребенка, пострадавшего в результате несчастного случая.</p>
<p>Каждый из нас желает безопасности своим близким, но не многие способны ее обеспечить. Теперь вы можете застраховать от несчастного случая всех членов своей семьи одновременно!</p>

<p><span class="greenb">Кому необходима страховая защита от несчастного случая?</span>
<ul>
    <li>Каждому взрослому, который из-за несчастного случая рискует потерять источник дохода, может испытывать затруднения с оплатой лечения, стать обузой для близких.</li>
    <li>Каждому, кто является кормильцем семьи (особые условия страхования предусматривают возможность пожизненной пенсии для супруга в случае смерти застрахованного).</li>
    <li>Людям, чья профессия связана с повышенным риском: охранники, водители, строители, журналисты и другие.</li>
    <li>Морякам, летчикам и авиапассажирам.</li>
    <li>Спортсменам.</li>
    <li>Семьям ("Сезонный" и "Семейный" полисы для членов одной семьи).</li>
    <li>Детям и подросткам.</li>
    <li>Одиноким и престарелым людям.</li>
    <li>Организациям, которые заинтересованы в повышении социальной защиты сотрудников (тарифы для организаций существенно ниже).</li>
</ul>
</p>

<p><span class="greenb">Что может быть застраховано по этому полису?</span>
<br>Вы можете предусмотреть в своем полисе практически все возможные последствия несчастного случая:
<ul>
    <li>физическая травма или увечье;</li>
    <li>временная утрата трудоспособности;</li>
    <li>инвалидность;</li>
    <li>смерть.</li>
</ul>
</p>

<h2>Страхование детей от несчастных случаев</h2>
<p>Вне зависимости от того, учится ваш ребенок в школе или только готовится к ней, отправился он в летний лагерь или поехал к бабушке. Благодаря страховому полису вы получаете реальную возможность компенсировать расходы на восстановление здоровья ребенка, пострадавшего в результате несчастного случая.
<br>На страхование принимаются дети в возрасте от 1 года до 17 лет (на момент заключения договора).</p>
<span class="greenb">Страховая защита  ребенка включает следующие риски:</span>
<ul>
    <li>травмы, включая переломы,</li>
    <li>вывихи,</li>
    <li>повреждения мягких тканей,</li>
    <li>повреждения  внутренних органов,</li>
    <li>ожоги,</li>
    <li>отравления</li>
    <li>другие последствия несчастного случая, включая гибель.</li>
</ul>

<p><span class="greenb">Как работает страховая защита?</span>
<ul>
    <li>Страховая защита действует 24 часа в сутки или во время пребывания в школе или детском дошкольном учреждении.</li>
    <li>Территория страхования - Россия или весь мир.</li>
    <li>Срок страхования - от 1 дня до 1 года. Например, отправляя ребенка в оздоровительный лагерь, родители могут приобрести специальную круглосуточную страховую защиту от несчастных случаев, действующую в течение всей смены.</li>
    <li>Страховую сумму, исходя из которой компания будет производить вам страховые выплаты, вы можете выбрать сами.</li>
    <li>Страховой взнос зависит от размера страховой суммы и возраста ребенка.</li>
    <li>РЕСО-Гарантия гарантирует выплату страхового обеспечения в течение пяти дней с момента несчастного случая.</li>
</ul>
</p>

<h2>Полис “Семейная защита-Универсал”</h2>
<p>Страховой полис "Семейная защита-Универсал" обеспечит надежную защиту от всех последствий несчастного случая:</p>
<ul>
    <li>смерть;</li>
    <li>инвалидность;</li>
    <li>физическая травма или увечье.</li>
</ul>
<p><b>Один полис для всей семьи</b></p>
<p>Этот полис - эффективная страховая защита от несчастных случаев для всей семьи в рамках одного договора. Родственниками, на которых может распространяться страховая защита, являются супруг, супруга, родители, дети, бабушки, дедушки, внуки, братья, сестры, усыновители, усыновленные. По одному полису можно застраховать неограниченное число родственников в возрасте от 1 года до 70 лет.</p>
<p>Страховой полис "Семейная защита-Универсал" можно оформить по одной из двух программ: <b>ЭКОНОМ</b> или <b>СТАНДАРТ</b>.</p>
<p>Выбрав программу, соответствующую вашим финансовым возможностям, вы получаете гарантию выплаты в пределах страховой суммы по каждому риску в случае несчастья с любым застрахованным членом семьи.</p>

<h2>Семейная защита-Универсал - Эконом</h2>
<p>При наступлении страхового случая вам (или выгодоприобретателю/наследникам) гарантирована выплата в размере:</p>
<ul>
    <li>50 000 руб. - в случае смерти застрахованного;</li>
    <li>40 000 руб. - в случае получения инвалидности I группы;</li>
    <li>30 000 руб. - в случае получения инвалидности II группы;</li>
    <li>20 000 руб. - в случае получения инвалидности III группы;</li>
    <li>процента от 30 000 руб., зависящего от тяжести травмы/увечья (по таблице страховых выплат) - в случае получения травмы/увечья.</li>
</ul>
<p>Цена полиса (размер страхового взноса) не зависит от того, сколько членов семьи вы застраховали, она определяется выбранной вами программой.  Полис по программе ЭКОНОМ стоит <b>1 500 руб</b>.</p>

<h2>Семейная защита-Универсал - Стандарт</h2>
