</ul>
Цена полиса (размер страхового взноса) не зависит от того, сколько членов семьи Вы застраховали, она определяется выбранной Вами программой. Полис по программе СТАНДАРТ стоит <b>4 500 руб</b>.
</p>
<p>РЕСО-Гарантия производит выплату страхового обеспечения в течение семи дней с момента подачи заявления и составления страхового акта. Такая выплата - ощутимая материальная помощь в трудную минуту, позволяющая решить вопросы по уходу, приобретению медикаментов, платной медицинской помощи и т.п.</p>

<h2>Полис "Подорожник"</h2>
<p>Быстрая и надежная защита от несчастного случая.
<br>От чего вы будете застрахованы:
<ul>
    <li>Физическая травма;</li>
    <li>постоянная утрата трудоспособности с установлением группы инвалидности;</li>
    <li>смерть, наступившая в течение года со дня несчастного случая, как прямое его следствие;</li>
    <li>смерть в результате дорожно-транспортного происшествия (ДТП) в течение года со дня происшествия, как прямое его следствие.</li>
</ul>
Полис «Подорожник» - защитит вас в случае ДТП на любом виде транспорта.
<p>


<p><b>Преимущества полиса "Подорожник":</b>
<ul>
    <li>Максимальный набор рисков;</li>
    <li>большие страховые суммы при невысокой стоимости полиса;</li>
    <li>включен риск ДТП (особо опасный в условиях большого города и использования любых средств транспорта);</li>
    <li>три варианта стоимости полиса;</li>
    <li>специальные цены для клиентов РЕСО-Гарантия;</li>
