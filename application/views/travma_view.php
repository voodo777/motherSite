<ul>
    <li>150 000 руб. - в случае смерти застрахованного;</li>
    <li>120 000 руб. - в случае получения инвалидности I группы;</li>
    <li>90 000 руб. - в случае получения инвалидности II группы;</li>
    <li>60 000 руб. - в случае получения инвалидности III группы;</li>
